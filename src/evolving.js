  '| `options` | uncommitted, discardable (the inbox / pool) |',
  '| `commit` | the commitment point; its `entry` gate is the Definition of Ready |',
  '| `active` | committed work in progress |',
  '| `loop` | a checkpoint with a feedback edge back (review / QA) |',
  '| `done` | the delivery point; its `entry` gate is the Definition of Done |',
  '',

// Static operating guide for the meta-flow: how to change the system of work.
// Universal — the procedure does not depend on a board's stages — so init drops
// it into .kanbento/ as a fixed asset (unlike AGENTS.md, which is compiled per
// board). Built as an array so inline `code` backticks need no escaping.


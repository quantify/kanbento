  '',
  'Changing the *system of work* — adding a stage, tightening a WIP limit, adding',
  'a gate or a card type — is itself work: you edit the board source under',
  '`.kanbento/` and recompile. It is **not** a plain file edit. In-flight cards may',
  'be left in a stage that no longer exists, so a structure change is a transaction',
  'that must be reconciled.',
  '',
  '## When to change it (usually: don’t)',
  '',
  'Structure earns its place — add it when the flow demands it, not on a hunch:',
  '',
  '- **recurrence** — a card *type* once N cards behave alike, or a *relation* type once an edge label recurs',
  '- **failure** — a review/gate where cards keep bouncing',
  '- **contention** — a WIP limit where concurrency measurably hurt cycle time',
  '',
  'A bare pool that just accumulates Options is a fine resting state. Don’t add a',
  'commitment point or stages speculatively — an empty pipeline is pure overhead.',
  'The board will not nag you to grow; grow when the evidence is in.',
  '',
  '## How to change it (the transaction)',
  '',
  '1. **Edit** `.kanbento/manifest.*` — add/rename/remove a stage, change a `wip`,',
  '   add an `entry` gate, add a `type`.',
  '2. **Preview** — `kanbento diff`: the structural changeset, and *which in-flight',
  '   cards get re-placed*. (After a bare edit, `BOARD.md` also lists stranded cards',
  '   under **⚠ Orphaned**.)',
  '3. **Apply** — `kanbento reconcile`: records a `StructureMutated` event, re-places',
  '   orphaned cards, re-baselines `compiled.json`, and regenerates this guide and',
  '   `AGENTS.md`. The board is consistent again.',
  '',
  'Never edit the manifest and keep working — you would leave orphaned cards and a',
